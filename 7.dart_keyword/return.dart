// return là từ khoá giúp trả về 1 cái gì đó
// được dùng trong các trường hợp muốn trả về khoá trị
// thường được sử dụng trong các hàm để trả về giá trị
// nếu trong biểu thức chỉ có 1 dòng trả về 1 giá trị thì có thể thay return bằng =>
int sum(int a, int b) {
  return a + b; //{} return giống =>
}

void main(List<String> args) {
  print(sum(1, 2));
}
